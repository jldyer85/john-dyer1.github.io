function fizzBuzz() {

  //loop through numbers 1-15
  for(var i = 1; i < 16; i++){

    //if multiple of 5 & 3 print fizzbuzz
    if(i % 3 === 0 && i % 5 === 0){
      console.log("fizzbuzz");

      //if multiple of 3 print fizz
    } else if(i % 3 === 0){
      console.log("fizz");

      //if multiple of 5 print buzz
    } else if(i % 5 === 0){
      console.log("buzz");
      
      //else print number
    } else {
      console.log(i);
    }
  }
}


