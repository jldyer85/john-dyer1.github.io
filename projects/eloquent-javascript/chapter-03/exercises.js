// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if(num1 < num2){
    return num1;
  } else {
    return num2;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
//base case
if(num === 0){
  return true;
} else if(num === 1){
  return false
//recursive call
} else if(num < 0){
  return isEven(-num);
} else {
  return isEven(num - 2);
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  var count = 0;
  for(var i = 0; i < string.length; i++){
    if(string[i] === char){
      count += 1;
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var count = 0;
  for(var i = 0; i < string.length; i++){
    if(string[i] === "B"){
      count += 1
    }
  }
  return count;
}

